day_number = input("Enter your day number(1-7): \n")

if day_number == "1":
    print("Saturday")
elif day_number == "2":
    print("Sundqay")
elif day_number == "3":
    print("Monday")
elif day_number == "4":
    print("Tuesday")
elif day_number == "5":
    print("Wednesday")
elif day_number == "6":
    print("Thursday")
elif day_number == "7":
    print("Friday")
else:
    print("Invalid input")